    {
        //
        $data = $request->json()->all();
        
        for ($i=0; $i<count($data); $i++) { 
            Userdetail::create([
                'user_id' => $data[$i]['user_id'],
                'question_id' => $data[$i]['question_id'],
                'is_correct' => $data[$i]['is_correct']
                ]);
        } 

        return redirect()->route('pages.index');
    }

